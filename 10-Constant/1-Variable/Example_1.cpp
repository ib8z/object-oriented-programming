/*
 * في المثال التالي قمنا بتعريف متغير ثابت إسمه X
 * واعطيناه قيمة وبعدها حاولنا تغير قيمته وحصلت مشكلة
 */
#include <iostream>
using namespace std;

int main()
{
    // هنا عرفنا المتغير كثابت واعطيناه قيمة
    const int x = 20;
    
    // هنا قمنا بطباعة قيمة المتغير
    cout << "X = " << x << endl;

    // هنا حاولنا تغير قيمته وحصلت مشكلة للاننا جعلنا المتغير معرف كثابت
    x = 25;
    
return 0;
}